num = 3

print(type(num))    # prints the type of variable
print(3 + 2)        # simple arithmetic operations
print(3 / 2)

print(3 // 2)       # floor division
print(3 ** 2)       # exponent
print(3 % 2)        # remainder (modulus)

num += 1            # adds 1 to num
num *= 10           # multiplies num with 10

print(abs(-3))          # absolute value = 3
print(round(3.45))      # rounds the integer
print(round(3.75, 1))   # round to 1 decimal

num_1 = 3
num_2 = 2
print(num_1 != num_2)   # comparison operators
print(num_1 >= num_2)
print(num_2 < num_2)

n = '100'
print(int(n) + 2)       # int method
